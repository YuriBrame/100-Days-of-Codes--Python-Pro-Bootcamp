#É possível usar a função round para arrendondar um número. como parâmetro da função é possível passar o número de casas decimais desejadas.
print(round(8 / 3, 2))
print(round(2.66666, 2))

#Outro meio é utilizando o duplo símbolo de divisão // isso irá converter o valor para um número inteiro e não float.
print(8 // 3)

#É possível armazenar valores em uma variável e trabalhar calcúlos em cima dela posteriormente.
result = 4 / 2
result /= 2
print(result)

score = 0
score += 1
print(score)